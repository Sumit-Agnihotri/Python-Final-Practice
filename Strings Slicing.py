# Slicing Strings
# Slicing is a way to extract a subsequence of elements from a sequence.
# In Python, we can slice strings using the following syntax:
# Example:
a=[1,2,3,4,5]
print(a[1:3])
# This is an example of String SLicing

# Slice from Start
# Example:
a=[1,2,3,4,5]
print(a[:3])

# Slice from End
# Example:
a=[1,2,3,4,5]
print(a[1:])