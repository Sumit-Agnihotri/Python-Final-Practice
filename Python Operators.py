# PYTHON OPERATORS
# Operators are used to perform operations on variables and values.
# Python divides the operators in the following groups:
# 1:- Arithmetic operators
# 2:- Assignment operators
# 3:- Comparison operators
# 4:- Logical operators
# 5:- Identity operators
# 6:- Membership operators
# 7:- Bitwise operators

a=2
b=3

print("1:- Arithmetic Operators")
print(a+b) # Addition
print(a-b) # Subtraction
print(a*b) # Multiplication
print(a/b) # Division
print(a%b) # Modulus
print(a**b) # Exponentiation
print(a//b) # Floor division

print("2:- Assignment Operators")
a+=b
print(a) # Add AND
a-=b
print(a) # Subtract AND
a*=b
print(a) # Multiply AND
a/=b
print(a) # Divide AND
a%=b
print(a) # Modulus AND
a**=b
print(a) # Exponentiation AND
a//=b
print(a) # Floor division AND

print("3:- Comparison Operators")
print(a==b) # Equal
print(a!=b) # Not equal
print(a>b) # Greater than
print(a<b) # Less than
print(a>=b) # Greater than or equal to
print(a<=b) # Less than or equal to

print("4:- Logical Operators")
print(a==b and a!=b) # and
print(a==b or a!=b) # or
print(not(a==b)) # not

print("5:- Identity Operators")
print(a is b) # is
print(a is not b) # is not
